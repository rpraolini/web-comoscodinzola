package it.asso.core.model.questionario;

public class Questionario {

	private String id_questionario;
	private String id_domanda;
	private String id_sezione;
	private String ordine;
	private String risposta;
	private String domanda;
	private String sezione;
	
	public String getId_questionario() {
		return id_questionario;
	}
	public void setId_questionario(String id_questionario) {
		this.id_questionario = id_questionario;
	}
	public String getId_domanda() {
		return id_domanda;
	}
	public void setId_domanda(String id_domanda) {
		this.id_domanda = id_domanda;
	}
	public String getId_sezione() {
		return id_sezione;
	}
	public void setId_sezione(String id_sezione) {
		this.id_sezione = id_sezione;
	}
	public String getOrdine() {
		return ordine;
	}
	public void setOrdine(String ordine) {
		this.ordine = ordine;
	}
	public String getRisposta() {
		return risposta;
	}
	public void setRisposta(String risposta) {
		this.risposta = risposta;
	}
	public String getDomanda() {
		return domanda;
	}
	public void setDomanda(String domanda) {
		this.domanda = domanda;
	}
	public String getSezione() {
		return sezione;
	}
	public void setSezione(String sezione) {
		this.sezione = sezione;
	}
	
	
}
