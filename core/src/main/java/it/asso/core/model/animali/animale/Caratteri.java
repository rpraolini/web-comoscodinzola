package it.asso.core.model.animali.animale;

public class Caratteri {

	private String id_caratteri;
	private String id_carattere;
	private String id_tipo_carattere;
	private String carattere;
	private String id_animale;
	private String note;
	private String icona;
	private String contesto;
	
	
	public String getId_caratteri() {
		return id_caratteri;
	}
	public void setId_caratteri(String id_caratteri) {
		this.id_caratteri = id_caratteri;
	}
	public String getId_carattere() {
		return id_carattere;
	}
	public void setId_carattere(String id_carattere) {
		this.id_carattere = id_carattere;
	}
	public String getCarattere() {
		return carattere;
	}
	public void setCarattere(String carattere) {
		this.carattere = carattere;
	}
	public String getId_animale() {
		return id_animale;
	}
	public void setId_animale(String id_animale) {
		this.id_animale = id_animale;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public String getIcona() {
		return icona;
	}
	public void setIcona(String icona) {
		this.icona = icona;
	}
	public String getContesto() {
		return contesto;
	}
	public void setContesto(String contesto) {
		this.contesto = contesto;
	}
	public String getId_tipo_carattere() {
		return id_tipo_carattere;
	}
	public void setId_tipo_carattere(String id_tipo_carattere) {
		this.id_tipo_carattere = id_tipo_carattere;
	}
	
	
}
