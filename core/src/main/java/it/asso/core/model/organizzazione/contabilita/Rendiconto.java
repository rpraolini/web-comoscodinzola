package it.asso.core.model.organizzazione.contabilita;

public class Rendiconto {
	
	private String id_sezione;
	private String id_sotto_sezione;
	private String id_cr_voce;
	private String id_cr_sotto_voce;
	private String descr_sezione;
	private String descr_sotto_sezione;
	private String descr_voce;
	private String descr_sotto_voce;
	private String tipo_sotto_sezione;
	
	
	public String getId_sezione() {
		return id_sezione;
	}
	public void setId_sezione(String id_sezione) {
		this.id_sezione = id_sezione;
	}
	public String getId_sotto_sezione() {
		return id_sotto_sezione;
	}
	public void setId_sotto_sezione(String id_sotto_sezione) {
		this.id_sotto_sezione = id_sotto_sezione;
	}
	public String getId_cr_voce() {
		return id_cr_voce;
	}
	public void setId_cr_voce(String id_cr_voce) {
		this.id_cr_voce = id_cr_voce;
	}
	public String getId_cr_sotto_voce() {
		return id_cr_sotto_voce;
	}
	public void setId_cr_sotto_voce(String id_cr_sotto_voce) {
		this.id_cr_sotto_voce = id_cr_sotto_voce;
	}
	public String getDescr_sezione() {
		return descr_sezione;
	}
	public void setDescr_sezione(String descr_sezione) {
		this.descr_sezione = descr_sezione;
	}
	public String getDescr_sotto_sezione() {
		return descr_sotto_sezione;
	}
	public void setDescr_sotto_sezione(String descr_sotto_sezione) {
		this.descr_sotto_sezione = descr_sotto_sezione;
	}
	public String getDescr_voce() {
		return descr_voce;
	}
	public void setDescr_voce(String descr_voce) {
		this.descr_voce = descr_voce;
	}
	public String getDescr_sotto_voce() {
		return descr_sotto_voce;
	}
	public void setDescr_sotto_voce(String descr_sotto_voce) {
		this.descr_sotto_voce = descr_sotto_voce;
	}
	public String getTipo_sotto_sezione() {
		return tipo_sotto_sezione;
	}
	public void setTipo_sotto_sezione(String tipo_sotto_sezione) {
		this.tipo_sotto_sezione = tipo_sotto_sezione;
	}
	
	

}
