package it.asso.core.model.adozioni;

public class TotaleAdottanti {

	private String tot_wallet;	
	private String tot_wallet_disponibile;
	private String tot_wallet_occupato;
	
	
	public String getTot_wallet() {
		return tot_wallet;
	}
	public void setTot_wallet(String tot_wallet) {
		this.tot_wallet = tot_wallet;
	}
	public String getTot_wallet_disponibile() {
		return tot_wallet_disponibile;
	}
	public void setTot_wallet_disponibile(String tot_wallet_disponibile) {
		this.tot_wallet_disponibile = tot_wallet_disponibile;
	}
	public String getTot_wallet_occupato() {
		return tot_wallet_occupato;
	}
	public void setTot_wallet_occupato(String tot_wallet_occupato) {
		this.tot_wallet_occupato = tot_wallet_occupato;
	}
		
}
