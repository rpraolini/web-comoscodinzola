package it.asso.core.model.contatto;

public class AttivitaContatto {

	private String id_contatto;
	private String soggetto;
	private String data_da;
	private String data_a;
	private String evento;
	
	
	public String getId_contatto() {
		return id_contatto;
	}
	public void setId_contatto(String id_contatto) {
		this.id_contatto = id_contatto;
	}
	public String getSoggetto() {
		return soggetto;
	}
	public void setSoggetto(String soggetto) {
		this.soggetto = soggetto;
	}
	public String getData_da() {
		return data_da;
	}
	public void setData_da(String data_da) {
		this.data_da = data_da;
	}
	public String getData_a() {
		return data_a;
	}
	public void setData_a(String data_a) {
		this.data_a = data_a;
	}
	public String getEvento() {
		return evento;
	}
	public void setEvento(String evento) {
		this.evento = evento;
	}
	
	
}
