package it.asso.core.model.contabilita;

public class Scadenziario {

	private String contatto;
	private String dt_rata;
	private String importo;
	private String prossima;
	
	
	public String getContatto() {
		return contatto;
	}
	public void setContatto(String contatto) {
		this.contatto = contatto;
	}
	public String getDt_rata() {
		return dt_rata;
	}
	public void setDt_rata(String dt_rata) {
		this.dt_rata = dt_rata;
	}
	public String getImporto() {
		return importo;
	}
	public void setImporto(String importo) {
		this.importo = importo;
	}
	public String getProssima() {
		return prossima;
	}
	public void setProssima(String prossima) {
		this.prossima = prossima;
	}
	
	
}
