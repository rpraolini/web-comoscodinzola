package it.asso.core.model.contatto;

public class PivaVies {
    
    private String codiceStato;
    private String piva;
    private String dtRichiesta;
    private String valido;
    private String ragSociale;
    private String indirizzo;
	public String getCodiceStato() {
		return codiceStato;
	}
	public void setCodiceStato(String codiceStato) {
		this.codiceStato = codiceStato;
	}
	public String getPiva() {
		return piva;
	}
	public void setPiva(String piva) {
		this.piva = piva;
	}
	public String getDtRichiesta() {
		return dtRichiesta;
	}
	public void setDtRichiesta(String dtRichiesta) {
		this.dtRichiesta = dtRichiesta;
	}
	public String getValido() {
		return valido;
	}
	public void setValido(String valido) {
		this.valido = valido;
	}
	public String getRagSociale() {
		return ragSociale;
	}
	public void setRagSociale(String ragSociale) {
		this.ragSociale = ragSociale;
	}
	public String getIndirizzo() {
		return indirizzo;
	}
	public void setIndirizzo(String indirizzo) {
		this.indirizzo = indirizzo;
	}
    
    
    
}
